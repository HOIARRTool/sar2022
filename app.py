# ===================== Helper: common =====================
def _limit_df(df, max_rows=MAX_ROWS, max_cols=MAX_COLS):
    df = df.copy()
    if df.shape[0] > max_rows:
        df = df.head(max_rows)
    if df.shape[1] > max_cols:
        df = df.iloc[:, :max_cols]
    return df

# ===================== Helper: PDF =====================
def extract_text_from_pdf(file):
    try:
        file.seek(0)
        reader = PdfReader(file)
        text = ""
        for page in reader.pages:
            page_text = page.extract_text()
            if page_text:
                text += page_text + "\n"
        return text
    except Exception as e:
        st.error(f"Error reading PDF file {getattr(file, 'name', '')}: {e}")
        return ""

# ===================== Helper: DOCX =====================
def extract_text_from_docx(file):
    try:
        file.seek(0)
        data = file.read()
        doc = Document(io.BytesIO(data))
        return "\n".join(p.text for p in doc.paragraphs)
    except Exception as e:
        st.error(f"Error reading DOCX file {getattr(file, 'name', '')}: {e}")
        return ""

# ===================== Helper: CSV =====================
def extract_text_from_csv(file):
    try:
        file.seek(0)
        df = pd.read_csv(file, dtype=str)
        df = _limit_df(df)
        return df.to_csv(index=False)
    except Exception as e:
        st.error(f"Error reading CSV file {getattr(file, 'name', '')}: {e}")
        return ""

